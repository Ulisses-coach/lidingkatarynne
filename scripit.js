document.addEventListener("DOMContentLoaded", function() {
    // Seleciona os elementos
    var modal = document.getElementById("modalZoom");
    var img = document.getElementById("imgOriginal");
    var modalImg = document.getElementById("imgAmpliada");
    var span = document.getElementsByClassName("fechar")[0];

    // Quando clicar na imagem, abre a modal
    img.onclick = function(){
        modal.style.display = "block";
        modalImg.src = this.src; // Define a imagem da modal como a da imagem original
    }

    // Quando clicar no (x), fecha a modal
    span.onclick = function() { 
        modal.style.display = "none";
    }

    // Fechar se clicar fora da imagem
    window.onclick = function(event) {
        if (event.target == modal) {
            modal.style.display = "none";
        }
    }
});

